    private void usernameAvailable()
    {
        String username1=jTextField3.getText().trim();
        flagAvailable=0;
        if(username1.indexOf(" ")!=-1)
        {
            jLabelAvailable.setText("No Spaces Allowed");
        }
        else if(username1.length()!=0 )
        {
            
            flagAvailable=0;
        jLabelAvailable.setText("Checking Availablity");
        int iRow=0;
        try {
            st = con.createStatement();
            rs = st.executeQuery("select * from waitinguser where username LIKE '" + username1 + "'");
            while (rs.next()) {
                iRow+=rs.getRow();
            }
            rs.close();

            rs = st.executeQuery("select * from users where username LIKE '" + username1 + "'");
            while (rs.next()) {
                iRow+=rs.getRow();
            }
            rs.close();
            st.close();
            if(iRow>=1)
                {
                    jLabelAvailable.setText("Not Available");
                    jTextField3.selectAll();
                    jTextField3.requestFocus();
                    iRow=0;
                }
                else
                {
                    jLabelAvailable.setText("Available");
                    flagAvailable=1;
                    iRow=0;
                }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,""+ "WRONG SYNTAX_CREATE QUERY");
            Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
        else
        {
             jLabelAvailable.setText("");
        }
        
    }

