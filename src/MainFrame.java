    private void passwordMatch(JPasswordField jp1,JPasswordField jp2,JLabel jlb)
    {
        flagMatch=0;
        if(String.copyValueOf(jp1.getPassword()).length()==0||String.copyValueOf(jp2.getPassword()).length()==0)
        {
            jlb.setText("");
        }
        else if(String.copyValueOf(jp2.getPassword()).equals(String.copyValueOf(jp1.getPassword())) && (int)(String.copyValueOf(jp2.getPassword()).charAt(0))<=122 && (int)(String.copyValueOf(jp2.getPassword()).charAt(0))>=97)
        {
            jlb.setText("Matched");
            flagMatch=1;
        }
        else
        {
            jlb.setText("Not Matched");
            jp2.selectAll();
            jp1.selectAll();
            flagMatch=0;
           // jPasswordField2.requestFocus();
        }
    }
